package Abgabe7.Aufgabe1;

public class Student {

	private String name;
	private String surname;

	public Student(String name, String surname) {
		super();
		this.name = name;
		this.surname = surname;
	}

	public String getName() {
		return name;
	}

	public String getSurname() {
		return surname;
	}
}
